/// Ключ для сопоставления: "фамилия_ио" (строчные, без точек).
String _keyFromFullName(String full) {
  final parts = full.trim().split(RegExp(r'\s+'));
  if (parts.length < 3) return full.toLowerCase().replaceAll(RegExp(r'\s+'), '_');
  final surname = parts[0].toLowerCase();
  final i1 = parts[1].isEmpty ? '' : parts[1][0].toLowerCase();
  final i2 = parts[2].isEmpty ? '' : parts[2][0].toLowerCase();
  return '${surname}_$i1$i2';
}

/// Из короткой подписи ("Чурилов А.В.", "А.В. Чурилов" и т.п.) извлекает фамилию и две буквы инициалов.
String? _keyFromShort(String short) {
  final raw = short.trim();
  if (raw.isEmpty) return null;
  final tokens = raw.split(RegExp(r'\s+'));
  if (tokens.isEmpty) return null;

  final initialLetters = <String>[];
  final surnameParts = <String>[];

  for (final t in tokens) {
    final s = t.replaceAll('.', '').trim();
    if (s.length == 1 && _isCyrillicLetter(s)) {
      initialLetters.add(s.toLowerCase());
    } else if (s.length == 2 && _isCyrillicLetter(s[0]) && _isCyrillicLetter(s[1])) {
      initialLetters.add(s[0].toLowerCase());
      initialLetters.add(s[1].toLowerCase());
    } else if (s.isNotEmpty && !RegExp(r'^[А-Яа-я]\.?$').hasMatch(s)) {
      surnameParts.add(s);
    }
  }

  if (surnameParts.isEmpty || initialLetters.length < 2) return null;
  final surname = surnameParts.join(' ').toLowerCase();
  final i1 = initialLetters[0];
  final i2 = initialLetters[1];
  return '${surname}_$i1$i2';
}

bool _isCyrillicLetter(String s) {
  if (s.isEmpty) return false;
  final c = s.codeUnitAt(0);
  return (c >= 0x0410 && c <= 0x044F) || c == 0x0401 || c == 0x0451;
}

Map<String, String>? _fullNameByKey;

Map<String, String> get _fullNameMap {
  _fullNameByKey ??= () {
    final map = <String, String>{};
    for (final full in _teachersFullNames) {
      final key = _keyFromFullName(full);
      map[key] = full;
    }
    return map;
  }();
  return _fullNameByKey!;
}

/// Возвращает полное ФИО одного преподавателя по короткой подписи.
String _resolveOne(String shortForm) {
  final key = _keyFromShort(shortForm);
  if (key == null) return shortForm;
  return _fullNameMap[key] ?? shortForm;
}

/// Возвращает полное ФИО преподавателя (или нескольких через запятую), если найдено совпадение по инициалам и фамилии;
/// иначе возвращает исходную строку (сокращённый вариант).
String resolveTeacherFullName(String shortForm) {
  final raw = shortForm.trim();
  if (raw.isEmpty) return shortForm;
  if (raw.contains(',')) {
    return raw.split(',').map((s) => _resolveOne(s.trim())).join(', ');
  }
  return _resolveOne(raw);
}
